import re

CPF = re.compile(r"(\d)(?!\1{10}|\1{2}\.\1{3}\.\1{3}-\1{2})(\d{10}|\d{2}\.\d{3}\.\d{3}-\d{2})")
MAY_BE_CPF = re.compile(
    r"(\d{0,11}|\d{0,3}((?<=\d{3})\.\d{0,3}((?<=\d{3}\.\d{3})\.\d{0,3}((?<=\d{3}\.\d{3}.\d{3})-\d{0,2})?)?)?)")
PHONE = re.compile(
    r"\(?([14689][1-9]|2[12478]|3[1234578]|5[1345]|7[13457])((?<=\(\d{2}))?\)?\s{0,2}9?\s?\d{4}[\s-]?\d{4}")
MAY_BE_PHONE = re.compile(
    r"\(?([14689][1-9]?|2[12478]?|3[1234578]?|5[1345]?|7[13457]?)?((?<=\(\d{2}))?\)?\s{0,2}9?\s?\d{0,4}[\s-]?\d{0,4}")
BRL = re.compile(r"(?:R\$ )?(?:(?:[1-9]\d{1,2}|\d)\.)?(?:[1-9]\d{1,2}|\d),\d{2}")
CAN_BE_BRL = re.compile(r"(?:R\$ )?(?:[1-9]\d{1,2}|\d)\.?(?:[1-9]\d{1,2}|\d)(?:,\d{1,2})?")
MAY_BE_BRL = re.compile(
    r"R?\$? ?(([1-9]\d{2}|[1-9]\d|[1-9])\.?\d{0,3}((?<=\d{3})(,\d{0,2}))?|([1-9]\d{2}|[1-9]\d|\d)(,\d{0,2})?)?")
EMAIL = re.compile(r"([A-Za-z0-9]+[.-_])*[A-Za-z0-9]+@[A-Za-z0-9-]+(\.[A-Z|a-z]{2,})+")
MAY_BE_EMAIL = re.compile(r"([A-Za-z0-9]+[.-_])*[A-Za-z0-9]+@[A-Za-z0-9-]+(\.[A-Z|a-z]{2,})+")
PRODUCT = re.compile(r"Cbrcrel|Ccrcfi|Epcfi")
DATE = re.compile(r"\d{2}/\d{2}/\d{4}")
